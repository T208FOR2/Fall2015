#include <iostream>

using namespace std;

int main()
{
    int a,b;

    cout << "Please write two numbers: " << endl;

    cin >> a;
    cin >> b;

    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << a << " % " << b << " = " << a % b << endl;




}
